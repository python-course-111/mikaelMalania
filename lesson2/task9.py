# შექმენით პროგრამა while loop-ისა და for loop-ის მეშვეობით,
# რომელიც დაადგენს კენტსა და ლუწ რიცხვებს 1-დან 100-მდე დიაპაზონში.
# დაბეჭდეთ კენტები და ლუწები ცალ-ცალკე.

# პირველი ვარიანტი

count = 0

while count < 100:
    count = count + 1
    if count % 2 == 0:
        print(f"ლუწი რიცხვი: {count}")
    else:
        print(f"კენტი რიცხვი: {count}")

# მეორე ვარიანტი

for i in range(1, 101):
    if i % 2 == 0:
        print(f"ლუწი რიცხვი: {i}")
    else:
        print(f"კენტი რიცხვი: {i}")
